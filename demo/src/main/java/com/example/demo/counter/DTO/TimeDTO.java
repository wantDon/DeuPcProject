package com.example.demo.counter.DTO;

public class TimeDTO {

    private int time_num;
    private int time_div;
    private int time_use;
    private int time_price;

    public int getTime_num() {
        return time_num;
    }

    public void setTime_num(int time_num) {
        this.time_num = time_num;
    }

    public int getTime_div() {
        return time_div;
    }

    public void setTime_div(int time_div) {
        this.time_div = time_div;
    }

    public int getTime_use() {
        return time_use;
    }

    public void setTime_use(int time_use) {
        this.time_use = time_use;
    }

    public int getTime_price() {
        return time_price;
    }

    public void setTime_price(int time_price) {
        this.time_price = time_price;
    }
}
